package Basic_Calculator.Calculator;

public class Subtract {
    public static int subtract_number(int num1, int num2) {
        int ans = num1 - num2;
        return ans;
    }

    public static float subtract_number(float num1, float num2) {
        float ans = num1 - num2;
        return ans;
    }

    public static double subtract_number(double num1, double num2) {
        double ans = num1 - num2;
        return ans;
    }
}
